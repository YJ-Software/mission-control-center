// Auto-generate .env.local on first run
function bootstrapEnvLocal() {
  const envPath = join(process.cwd(), '.env.local')
  if (existsSync(envPath)) return

  console.log('[Bootstrap] First run detected, generating .env.local ...')

  const password = randomBytes(16).toString('base64url')
  const secret = randomBytes(32).toString('base64url')

  // Try to read gateway token from ~/.openclaw/openclaw.json
  let gatewayToken = ''
  try {
    const configPath = join(homedir(), '.openclaw', 'openclaw.json')
    const config = JSON.parse(readFileSync(configPath, 'utf8'))
    gatewayToken = config?.gateway?.auth?.token || ''
  } catch {}

  const lines = [
    '# Auto-generated by Mission Control on first run',
    '',
    '# OpenClaw Gateway',
    'OPENCLAW_GATEWAY_WS=ws://127.0.0.1:18789',
    'OPENCLAW_GATEWAY_HTTP=http://127.0.0.1:18789',
    `OPENCLAW_TOKEN=${gatewayToken}`,
    '',
    '# Authentication',
    `AUTH_PASSWORD=${password}`,
    `AUTH_SECRET=${secret}`,
    '',
    '# Server',
    'PORT=3737',
    'HOST=0.0.0.0',
    '',
  ]

  writeFileSync(envPath, lines.join('\n'))
  console.log('[Bootstrap] .env.local created')
  console.log(`[Bootstrap] Dashboard password: ${password}`)
  console.log('[Bootstrap] Save this password — it won\'t be shown again.')
  if (gatewayToken) {
    console.log('[Bootstrap] Gateway token auto-detected from ~/.openclaw/openclaw.json')
  } else {
    console.warn('[Bootstrap] No gateway token found. Set it in Settings page or .env.local')
  }
}

