// -----------------------------------------------------------------------------
// OpenClaw device identity (Ed25519)
// Newer Gateways (2026.4+) clear requested scopes for token-only connections
// without a device identity, leaving every RPC with "missing scope: operator.read".
// Generate a persistent Ed25519 keypair and sign the connect.challenge payload;
// localhost connections are auto-paired by the Gateway so no manual approval is
// needed.
// -----------------------------------------------------------------------------

function base64UrlEncode(buf: Buffer): string {
  return buf.toString('base64').replace(/\+/g, '-').replace(/\//g, '_').replace(/=+$/, '')
}

interface DeviceIdentity {
  privateKey: KeyObject
  publicKeyB64Url: string
  deviceId: string
}

function loadOrCreateDeviceIdentity(): DeviceIdentity {
  const dir = join(homedir(), '.mission-control')
  const keyPath = join(dir, 'gateway-device-key.pem')
  let privateKey: KeyObject
  try {
    if (existsSync(keyPath)) {
      privateKey = createPrivateKey({ key: readFileSync(keyPath, 'utf8'), format: 'pem' })
    } else {
      const { privateKey: pk } = generateKeyPairSync('ed25519')
      const pem = pk.export({ type: 'pkcs8', format: 'pem' }) as string
      try { writeFileSync(keyPath, pem, { mode: 0o600 }) } catch { /* best-effort persist */ }
      privateKey = pk
    }
  } catch (err) {
    console.warn('[Gateway] device key persist failed, using in-memory only:', (err as Error).message)
    privateKey = generateKeyPairSync('ed25519').privateKey
  }
  const publicKey = createPublicKey(privateKey)
  const jwk = publicKey.export({ format: 'jwk' }) as { x?: string }
  if (!jwk.x) throw new Error('Failed to extract Ed25519 public key')
  // JWK `x` already is base64url of the raw 32-byte key.
  const publicKeyB64Url = jwk.x
  const rawPub = Buffer.from(publicKeyB64Url.replace(/-/g, '+').replace(/_/g, '/')
    + '='.repeat((4 - publicKeyB64Url.length % 4) % 4), 'base64')
  const deviceId = createHash('sha256').update(rawPub).digest('hex')
  return { privateKey, publicKeyB64Url, deviceId }
}

const DEVICE_IDENTITY = loadOrCreateDeviceIdentity()
