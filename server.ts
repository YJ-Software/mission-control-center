// Auto-detect gateway token from ~/.openclaw/openclaw.json if not in env
function resolveGatewayToken(): string {
  const envToken = process.env.OPENCLAW_TOKEN
  if (envToken) return envToken

  try {
    const configPath = join(homedir(), '.openclaw', 'openclaw.json')
    const config = JSON.parse(readFileSync(configPath, 'utf8'))
    const token = config?.gateway?.auth?.token
    if (token) {
      console.log('[Gateway] Token auto-detected from ~/.openclaw/openclaw.json')
      return token
    }
  } catch {}

  console.warn('[Gateway] No token found in env or ~/.openclaw/openclaw.json')
  return ''
}

